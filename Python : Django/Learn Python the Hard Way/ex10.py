tabby_cat = "\tI'm tabbed in."
# assigns string with escaped newline to persian_cat
persian_cat = "I'm split\non a line."
# assigns string with escaped backslash to backslash_cat
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
